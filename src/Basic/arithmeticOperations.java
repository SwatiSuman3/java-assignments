package Basic;

public class arithmeticOperations {
    public static void main(String[] args) {
    int a =10 , b = 5;
    float x =24 , y = 12;
    double p = 48.9 , q = 24.4;
    System.out.println("integer result :" +(a+b));
    System.out.println("float result :" +(x-y));
    System.out.println("double result :" +(p*q));
    System.out.println("division result :" +(x/y));
    }
}
